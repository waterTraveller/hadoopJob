package com.topeastic.hadoop.entity;

public class Job {

	private String jobName;
	private String jobInputPath;
	private String jobOutputPath;
	private String className;
	private String jobId;
	private char jobState;
	private String jobStartTime;
	private String jobEndTime;
	private String jobJarName;
	
	private Job(){}
	
	public static Job getInstance(){
		return new Job();
	} 
	
	public String getJobId() {
		return jobId;
	}

	public void setJobId(String jobId) {
		this.jobId = jobId;
	}

	public char getJobState() {
		return jobState;
	}

	public void setJobState(char jobState) {
		this.jobState = jobState;
	}

	public String getJobStartTime() {
		return jobStartTime;
	}

	public void setJobStartTime(String jobStartTime) {
		this.jobStartTime = jobStartTime;
	}

	public String getJobEndTime() {
		return jobEndTime;
	}

	public void setJobEndTime(String jobEndTime) {
		this.jobEndTime = jobEndTime;
	}

	public String getJobJarName() {
		return jobJarName;
	}

	public void setJobJarName(String jobJarName) {
		this.jobJarName = jobJarName;
	}

	public String getClassName() {
		return className;
	}

	public void setClassName(String className) {
		this.className = className;
	}

	public String getJobName() {
		return jobName;
	}


	public void setJobName(String jobName) {
		this.jobName = jobName;
	}


	public String getJobInputPath() {
		return jobInputPath;
	}


	public void setJobInputPath(String jobInputPath) {
		this.jobInputPath = jobInputPath;
	}


	public String getJobOutputPath() {
		return jobOutputPath;
	}


	public void setJobOutputPath(String jobOutputPath) {
		this.jobOutputPath = jobOutputPath;
	}



	@Override
	public String toString() {
		return "Job [jobName=" + jobName + ", jobInputPath=" + jobInputPath
				+ ", jobOutputPath=" + jobOutputPath + ", className="
				+ className + ", jobId=" + jobId + ", jobState=" + jobState
				+ ", jobStartTime=" + jobStartTime + ", jobEndTime="
				+ jobEndTime + ", jobJarName=" + jobJarName + "]";
	}

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		// TODO Auto-generated method stub

	}

}
